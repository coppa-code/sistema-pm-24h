// üßÆ CALCULAR IDADE SEGURA
function calculateAge(dateString) {
    try {
        if (!dateString || !dateString.includes('/')) {
            console.log(`‚ö†Ô∏è Data inv√°lida para c√°lculo de idade: ${dateString}`);
            return 0;
        }
        
        const dateParts = dateString.split('/');
        if (dateParts.length < 3) {
            console.log(`‚ö†Ô∏è Data incompleta para c√°lculo de idade: ${dateString}`);
            return 0;
        }
        
        const [day, month, year] = dateParts;
        
        if (!day || !month || !year) {
            console.log(`‚ö†Ô∏è Partes da data vazias: ${dateString}`);
            return 0;
        }
        
        const birth = new Date(parseInt(year), parseInt(month) - 1, parseInt(day));
        const today = new Date();
        
        let age = today.getFullYear() - birth.getFullYear();
        const monthDiff = today.getMonth() - birth.getMonth();
        
        if (monthDiff < 0 || (monthDiff === 0 && today.getDate() < birth.getDate())) {
            age--;
        }
        
        return age > 0 ? age : 0;
    } catch (error) {
        console.error('‚ùå Erro ao calcular idade:', error.message);
        return 0;
    }
}

// üìÖ VERIFICAR QUEM FAZ ANIVERS√ÅRIO AMANH√É - VERS√ÉO SEGURA
function checkTomorrowBirthdays(birthdays) {
    const tomorrow = new Date();
    tomorrow.setDate(tomorrow.getDate() + 1);
    
    const tomorrowDay = tomorrow.getDate().toString().padStart(2, '0');
    const tomorrowMonth = (tomorrow.getMonth() + 1).toString().padStart(2, '0');
    
    console.log(`üîç Verificando anivers√°rios para AMANH√É: ${tomorrowDay}/${tomorrowMonth}`);
    
    const tomorrowBirthdays = birthdays.filter(birthday => {
        try {
            // ‚úÖ VERIFICA√á√ïES DE SEGURAN√áA
            if (!birthday) {
                console.log('‚ö†Ô∏è Anivers√°rio √© null/undefined');
                return false;
            }
            
            if (!birthday.date || birthday.date === '') {
                console.log(`‚ö†Ô∏è Data vazia para: ${birthday.name || 'Nome n√£o informado'}`);
                return false;
            }
            
            // ‚úÖ VERIFICAR SE A DATA CONT√âM BARRA
            if (!birthday.date.includes('/')) {
                console.log(`‚ö†Ô∏è Formato de data inv√°lido para ${birthday.name}: ${birthday.date}`);
                return false;
            }
            
            const dateParts = birthday.date.split('/');
            
            // ‚úÖ VERIFICAR SE TEM PELO MENOS DIA E M√äS
            if (dateParts.length < 2) {
                console.log(`‚ö†Ô∏è Data incompleta para ${birthday.name}: ${birthday.date}`);
                return false;
            }
            
            const day = dateParts[0];
            const month = dateParts[1];
            
            // ‚úÖ VERIFICAR SE DIA E M√äS N√ÉO S√ÉO VAZIOS
            if (!day || !month || day.trim() === '' || month.trim() === '') {
                console.log(`‚ö†Ô∏è Dia ou m√™s vazio para ${birthday.name}: ${birthday.date}`);
                return false;
            }
            
            // ‚úÖ APLICAR padStart COM SEGURAN√áA
            const birthdayDay = day.toString().trim().padStart(2, '0');
            const birthdayMonth = month.toString().trim().padStart(2, '0');
            
            const match = birthdayDay === tomorrowDay && birthdayMonth === tomorrowMonth;
            
            if (match) {
                console.log(`üéÇ ENCONTRADO: ${birthday.graduation || 'Sem gradua√ß√£o'} ${birthday.name || 'Sem nome'} - ${birthday.date}`);
            }
            
            return match;
            
        } catch (error) {
            console.error(`‚ùå Erro ao processar anivers√°rio de ${birthday.name || 'Nome desconhecido'}:`, error.message);
            console.error(`   Data problem√°tica: "${birthday.date}"`);
            return false;
        }
    });
    
    console.log(`üìä Total de anivers√°rios AMANH√É: ${tomorrowBirthdays.length}`);
    return tomorrowBirthdays;
}

